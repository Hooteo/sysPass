namespace SP\Core\Context;

/**
 * Class SessionUtil
 */
final class SessionUtil
{
    /**
     * Limpiar la sesión del usuario
     */
    public static function cleanSession(): void
    {
        session_start();

        $_SESSION = [];

        if (ini_get("session.use_cookies")) {
            $params = session_get_cookie_params();
            setcookie(
                session_name(),
                '',
                time() - 42000,
                $params["path"],
                $params["domain"],
                $params["secure"],
                $params["httponly"]
            );
        }

        session_destroy();
    }
}
